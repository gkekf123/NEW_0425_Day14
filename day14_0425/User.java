package day14_0425;

public class User {

	private String name;
	private int age;
	private int rnn;
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getRnn() {
		return rnn;
	}
	public void setRnn(int rnn) {
		this.rnn = rnn;
	}
	
	
	
}
